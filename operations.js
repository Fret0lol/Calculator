export default {
  divide(numberOne, numberTwo) {
    return numberOne / numberTwo;
  },
  
  multiply(numberOne, numberTwo) {
    return numberOne * numberTwo;
  },
  
  subtract(numberOne, numberTwo) {
    return numberOne - numberTwo;
  },
  
  add(numberOne, numberTwo) {
    return numberOne + numberTwo;
  }
}
